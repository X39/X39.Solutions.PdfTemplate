namespace X39.Solutions.PdfTemplate.Data;

/// <summary>
/// Enum to describe the vertical alignment.
/// </summary>
public enum EVerticalAlignment : sbyte
{
    /// <summary>
    /// Aligns the content to the top.
    /// </summary>
    Top,
    /// <summary>
    /// Aligns the content to the center.
    /// </summary>
    Center,
    /// <summary>
    /// Aligns the content to the bottom.
    /// </summary>
    Bottom,
    /// <summary>
    /// Stretches the content to fill the available space.
    /// </summary>
    Stretch,
}