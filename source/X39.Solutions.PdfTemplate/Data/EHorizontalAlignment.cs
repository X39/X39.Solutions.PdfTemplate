namespace X39.Solutions.PdfTemplate.Data;

/// <summary>
/// Enum to describe the horizontal alignment.
/// </summary>
public enum EHorizontalAlignment : sbyte
{
    /// <summary>
    /// Aligns the content to the left.
    /// </summary>
    Left,
    /// <summary>
    /// Aligns the content to the center.
    /// </summary>
    Center,
    /// <summary>
    /// Aligns the content to the right.
    /// </summary>
    Right,
    /// <summary>
    /// Stretches the content to fill the available space.
    /// </summary>
    Stretch,
}